#include<stdio.h>
#include<math.h>
int main()
{
    int n,s,sq,r1,r2,s1=0,s2=0;
    scanf("%d",&n);
    s=n*n;
    while(s!=0)
    {
        r1=s%10;
        s/=10;
        s1=s1*10+r1;
    }
    sq=sqrt(s1);
    while(sq!=0)
    {
        r2=sq%10;
        sq/=10;
        s2=s2*10+r2;
    }
    if(s2==n)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}